console.log("Javascript operators");


let a = 5;
let b = 10;

// Arethmatic operators
console.log("Arethmatic operators");

console.log("a + b = ", a + b)
console.log("a - b = ", a - b)
console.log("a * b = ", a * b)
console.log("a / b = ", a / b)
console.log("a % b = ", a % b)
console.log("a ** b = ", a ** b)
console.log("++a = ", ++a) // 1st adds and then print 
console.log("a++ = ", a++) // 1st print and then adds
console.log("--a = ", --a) // 1st Subtracts and then print
console.log("a-- = ", a--) // 1st print and then subtracts

//Assignment operators
console.log("Assignment operators");

console.log("a = ", a); // a = 5
console.log("b = ", b); // b = 10
console.log("a += b = ", a += b); // a = 15
console.log("a -= b = ", a -= b); // a = 5
console.log("a *= b = ", a *= b); // a = 50
console.log("a /= b = ", a /= b); // a = 5
console.log("a = ", a); // a = 5
console.log("b = ", b); // b = 10
console.log("a %= b = ", a %= b); // a = 5
console.log("a **= b = ", a **= b); // a ^ b (a power b)

// Comparison operator 
console.log("Comparison operators");

console.log("a == b ", a == b);
console.log("a != b ", a != b);
console.log("a <= b ", a <= b);
console.log("a >= b ", a >= b);
console.log("a < b ", a < b);
console.log("a > b ", a > b);
console.log("a === b ", a === b); // return true if types of operands matches
console.log("a !== b ", a !== b); // return true if types of operands not matches


//Logical operators &&, ||, !
console.log("Logical operators");
console.log("a = ", a); // a = 9765625
console.log("b = ", b); // b = 10
console.log("a < b && a > 0", a < b && a > 0); // false
console.log("a > b && a > 0", a > b && a > 0); // true
console.log("a < b || a > 0", a < b || a > 0); // true
